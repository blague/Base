package org.zj.pojo;

public class City {

    private String id;
    private String name;
    private String code;
    private String en;
    private String pyjsm;

    public City() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getEn() {
        return en;
    }

    public void setEn(String en) {
        this.en = en;
    }

    public String getPyjsm() {
        return pyjsm;
    }

    public void setPyjsm(String pyjsm) {
        this.pyjsm = pyjsm;
    }
}
